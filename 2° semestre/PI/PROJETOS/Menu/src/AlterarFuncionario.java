    private void buttonAlterarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAlterarFuncionarioActionPerformed
        try {
            String id, nome, endereco, bairro, cidade, estado, telefone, email;
            id = textIdFuncionario.getText();
            nome = textNomeFuncionario.getText();
            endereco = textEnderecoFuncionario.getText();
            bairro = textBairroFuncionario.getText();
            cidade = textCidadeFuncionario.getText();
            estado = textEstadoFuncionario.getText();
            telefone = textTelefoneFuncionario.getText();
            email = textEmailFuncionario.getText();

            if (id.equals("") || nome.equals("") || endereco.equals("") || bairro.equals("")||cidade.equals("")||estado.equals("")||telefone.equals("")||email.equals("")) {
                JOptionPane.showMessageDialog(null, "Insira todas as informações: ");
            } else {
                PreparedStatement st = conex.prepareStatement("UPDATE funcionario SET nome = ?, endereco = ?, bairro = ?, cidade = ?, estado = ?, telefone = ?, email = ? WHERE id = ?");
                
                st.setString(1, nome);
                st.setString(2, endereco);
                st.setString(3, bairro);
                st.setString(4, cidade);
                st.setString(5, estado);
                st.setString(6, telefone);
                st.setString(7, email);
                st.setString(8, id);
                st.executeUpdate();
                JOptionPane.showMessageDialog(null, "Funcionario Alterado.");
                textIdFuncionario.setText("");
                textNomeFuncionario.setText("");
                textEnderecoFuncionario.setText("");
                textBairroFuncionario.setText("");
                textCidadeFuncionario.setText("");
                textEstadoFuncionario.setText("");
                textTelefoneFuncionario.setText("");
                textEmailFuncionario.setText("");
                textIdFuncionario.requestFocus();
            }

        } catch (SQLException ex) {
            Logger.getLogger(CadastrarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buttonAlterarFuncionarioActionPerformed

