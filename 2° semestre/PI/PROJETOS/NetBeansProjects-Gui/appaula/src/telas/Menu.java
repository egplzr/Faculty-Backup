    private void itmExcluirDepartamentosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itmExcluirDepartamentosActionPerformed
        //pedir o usuario que vai excluir
        String id;
        id = JOptionPane.showInputDialog("ID do Departamento: ");
        //conectar ao banco de dados
        try {
            //Conectar ao banco de dados empresa
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectar = DriverManager.getConnection("jdbc:mysql://localhost:3306/empresagui", "root", "");
            //3 - enviar os dados para a tabela BD 
            PreparedStatement st = conectar.prepareStatement("DELETE FROM departamento WHERE  ID = ?");
            st.setString(1, id);
            int retorno = st.executeUpdate(); // variavel para guardar o retorno do delete, se foi deletado ou não
            //4- exibir uma mensagem para o usuario
            if (retorno == 1) {
                JOptionPane.showMessageDialog(null, "Departamento excluído com sucesso");

            } else {
                JOptionPane.showMessageDialog(null, "Departamento não encontrado");
            }
            // 5 - limpar os campos
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_itmExcluirDepartamentosActionPerformed

