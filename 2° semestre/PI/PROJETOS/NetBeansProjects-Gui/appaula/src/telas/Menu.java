    private void itmAlterarUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itmAlterarUsuariosActionPerformed
        // 1 - obter o nome do usuario que vai alterar
        String u;
        u = JOptionPane.showInputDialog("Usuário a alterar");
        try {
            // 2- Conectar ao banco de dados empresa
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectar = DriverManager.getConnection("jdbc:mysql://localhost:3306/empresagui", "root", "");
            //3 - BUSCAR O USUARIO NO BC    
            PreparedStatement st = conectar.prepareStatement("SELECT * FROM usuarios WHERE usuario = ?");
            st.setString(1, u);
            ResultSet usuario = st.executeQuery(); //COMANDO EXECUTANDO O SELECT    
            //4- VERIFICAR SE ENCONTROU USUARIO
            if (usuario.next()) { //NEXT É "SE ENCONTROU"
                // 5 - ENVIAR OS DADOS DO USUARIO PARA A TELA AlterarUsuarios
                String usu, sen, car;
                usu = usuario.getString("usuario");
                sen = usuario.getString("senha");
                car = usuario.getString("cargo");
                new AlterarUsuarios(usu, sen, car).setVisible(true);

            } else { // NÃO ENCONTROU
                JOptionPane.showMessageDialog(null, "Usuário não encontrado");
            }
            // 5 - limpar os campos
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_itmAlterarUsuariosActionPerformed

