    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //1 - pegar os dados digitados nos campos e guardar em variaveis
       String id,n,f;
       id = txtId.getText();
       n = txtNome.getText();
       f = txtFone.getText();
       //Conectar ao banco de dados empresa
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectar = DriverManager.getConnection("jdbc:mysql://localhost:3306/empresagui","root","");
            //3 - enviar os dados para a tabela BD 
            PreparedStatement st = conectar.prepareStatement("INSERT INTO departamento VALUES(?,?,?)");
            st.setString(1, id);
            st.setString(2, n);
            st.setString(3, f);
            st.executeUpdate(); //executa o comando INSERT
            //4- exibir uma mensagem para o usuario
            JOptionPane.showMessageDialog(null, "Cadastro Feito");
            // 5 - limpar os campos
            txtId.setText("");
            txtNome.setText("");
            txtFone.setText("");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null,"Entre em contato com o suporte e informe o erro:" +ex.getMessage());
        } catch (SQLException ex) {
            String erro = ex.getMessage();
            if(erro.contains("Duplicate entry")){
                JOptionPane.showMessageDialog(null, "Departamento j√° cadastrado");
            }else{
                JOptionPane.showMessageDialog(null,"Entre em contato com o suporte e informe o erro:" +ex.getMessage());
            }
            
        }                                        
    }//GEN-LAST:event_btnSalvarActionPerformed

